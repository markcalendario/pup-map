function handleCloseModalBtnClick() {
  const modal = document.getElementById("modal");
  modal.remove();
}

function showModal(location) {
  const modalBackground = document.createElement("div");
  modalBackground.className = "modal-background";
  modalBackground.id = "modal";

  const modalCloseBtn = document.createElement("button");
  modalCloseBtn.innerHTML = "&times;";
  modalCloseBtn.className = "close";
  modalCloseBtn.id = "modal-close";
  modalCloseBtn.onclick = handleCloseModalBtnClick;

  const modalBox = document.createElement("div");
  modalBox.className = "modal-box";

  const locationImage = document.createElement("img");
  locationImage.alt = "preview";
  locationImage.src = "./assets/locations/" + locations[location].imageName;

  const locationName = document.createElement("p");
  locationName.className = "location-name";
  locationName.innerText = locations[location].title;

  const locationDescription = document.createElement("p");
  locationDescription.className = "description";
  locationDescription.innerText = locations[location].description;

  modalBox.appendChild(modalCloseBtn);
  modalBox.appendChild(locationImage);
  modalBox.appendChild(locationName);
  modalBox.appendChild(locationDescription);
  modalBackground.appendChild(modalBox);

  document.getElementsByTagName("body")[0].appendChild(modalBackground);
}

let hideTimeout;

function handleNavGuideMouseMove() {
  console.log("e");
  const navGuide = document.getElementById("navigation-guide");

  navGuide.style.visibility = "hidden";
  navGuide.style.opacity = "0";

  clearTimeout(hideTimeout);

  hideTimeout = setTimeout(() => {
    const modal = document.getElementById("modal");
    if (modal) return;

    navGuide.style.visibility = "unset";
    navGuide.style.opacity = "1";
  }, 5000);
}

window.addEventListener("mousemove", handleNavGuideMouseMove);
window.addEventListener("touchstart", handleNavGuideMouseMove);
window.addEventListener("touchmove", handleNavGuideMouseMove);
